    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        try{
                
        if (nombreBarTexField.getText().isEmpty()) {   
            throw new ExcepcionRegistroNomBar(1);
//          errorBar.setText("Este campo no puede estar vacio"); 
        }
        if(nombreUsuarioTextField.getText().isEmpty()){
            throw new ExcepcionRegistroNomUsuario(1);
        }
        if (contrasenaTextFiled.getText().isEmpty()) {
           throw new ExcepcionRegistroContrasena(1);
        }
        if (!nombreBarTexField.getText().isEmpty()&&!nombreUsuarioTextField.getText().isEmpty()&&!contrasenaTextFiled.getText().isEmpty()) {
            
            if (ListaUsuarios.comprobarNombreBar(nombreBarTexField.getText())==true &&ListaUsuarios.comprobarUsuario(nombreUsuarioTextField.getText())==true) {
            ListaUsuarios.llenarLista(new Usuario(nombreBarTexField.getText(), nombreUsuarioTextField.getText(), contrasenaTextFiled.getText()));
            ListaDeAcceso.setListaAcceso(nombreUsuarioTextField.getText(), nombreUsuarioTextField.getText());
            Inicio.ventanaInicio.setVisible(true);
            dispose();
            }else{
                System.out.println("El nombre de Usuario o El nombre del bar ya existen");
            }
        }
        }catch(ExcepcionRegistroNomBar e){
            errorBar.setText(e.getMessage());
        }catch(ExcepcionRegistroNomUsuario e){
            errorUsuario.setText(e.getMenssage());
        }catch(ExcepcionRegistroContrasena e){
            errorContrasena.setText(e.getMessage());
        }
        
        
        
    }//GEN-LAST:event_GuardarActionPerformed

